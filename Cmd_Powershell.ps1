#  Cette commande permet de lister toutes les commandes disponibles dans PowerShell.
Get-Commandes

# Utilisé pour obtenir de l'aide sur la commande powershell
Get-Help

# Permet d'obtenir des informations sur les processus en cours d'exécution sur le système.
Get-Process

#  Utilisé pour obtenir des information sur les services windows
Get-Service

#  Permet de lister les fichiers et dossiers dans un répertoire donné.
Get-ChildItem

# Permet de définir la politique d'exécution des scripts dans PowerShell.
Set-ExecutionPolicy

# Utilisée pour créer de nouveaux fichiers, dossiers ou autres éléments dans le système de fichiers.
New-Item

# Permet de supprimer un fichier ou un dossier.
Remove-Item

# Permet de supprimer un fichier ou un dossier.
Remove-Item -Force

# Permet de supprimer un fichier ou un dossier.
Remove-Item -Recurse

# Permet de copier des fichiers ou des dossiers d'un emplacement a un autre
Copy-Item

# Utilisée pour déplacer des fichiers ou des dossiers d'un emplacement à un autre.
Move-Item

#  Utilisée pour afficher le contenu d'un fichier.
Get-Content

# Permet de modifier le contenu d'un fichier.
Set-Content

#  Utilisée pour exécuter des commandes sur des ordinateurs distants.
Invoke-Command

#  Permet de démarrer un nouveau processus.
Start-Process

# Utilisée pour arrêter un processus en cours d'exécution.
Stop-Process
